import { readFile, writeFile } from 'node:fs/promises';
import { relative, resolve } from 'node:path';

import {
  getContentValidationTarget,
  normalizeContentModel,
  validateStoryExport,
} from '../packages/story-core/src/index.js';

const root = resolve(new URL('..', import.meta.url).pathname);
const defaultOutputPath = resolve(root, 'packages/story-content/src/storyData.js');

function formatPathLabel(path) {
  const label = relative(root, path);
  return label && !label.startsWith('..') ? label : path;
}

function printUsage() {
  console.log(`Usage:
  npm run promote:content -- <release-export.json> [--dry-run] [--out <path>]

Promotes an accepted admin Publish export into packages/story-content for bundled player builds.

Options:
  --dry-run       Validate and report the generated target without writing files.
  --out <path>   Write the generated storyData.js module to a custom path.`);
}

function parseArgs(argv) {
  const options = {
    dryRun: false,
    outputPath: defaultOutputPath,
    sourcePath: '',
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];

    if (arg === '--help' || arg === '-h') {
      return { ...options, help: true };
    }

    if (arg === '--dry-run') {
      options.dryRun = true;
      continue;
    }

    if (arg === '--out') {
      const next = argv[index + 1];
      if (!next) throw new Error('--out requires a path.');
      options.outputPath = resolve(root, next);
      index += 1;
      continue;
    }

    if (arg.startsWith('--')) {
      throw new Error(`Unknown option: ${arg}`);
    }

    if (options.sourcePath) {
      throw new Error(`Unexpected extra argument: ${arg}`);
    }

    options.sourcePath = resolve(root, arg);
  }

  return options;
}

function normalizeFlowMap(flowMap) {
  if (flowMap && typeof flowMap === 'object' && Array.isArray(flowMap.rules)) {
    return {
      ...flowMap,
      rules: flowMap.rules.map((rule) => ({ ...rule })),
    };
  }

  if (flowMap && typeof flowMap === 'object') {
    const rules = [];

    Object.entries(flowMap).forEach(([sourceChoiceId, targetIds]) => {
      if (!Array.isArray(targetIds)) return;

      targetIds.forEach((targetEnvelopeId, index) => {
        rules.push({
          id: `legacy-${sourceChoiceId}-${targetEnvelopeId}-${index}`,
          sourceChoiceId,
          sourceFieldId: '',
          operator: 'always',
          value: '',
          targetEnvelopeId,
        });
      });
    });

    return { rules };
  }

  return { rules: [] };
}

function createPromotedContent(source) {
  const target = getContentValidationTarget(source);
  const flowMap = normalizeFlowMap(target.flowMap);
  const content = normalizeContentModel(target.content);

  return {
    ...content,
    defaultFlowMap: flowMap,
  };
}

function formatStoryDataModule(content, sourcePath) {
  const sourceLabel = formatPathLabel(sourcePath);
  const contentLiteral = JSON.stringify(content, null, 2);

  return `// Generated by scripts/promote-release-content.mjs from ${sourceLabel}.
// Edit story content in apps/admin, export a release JSON, then run the promotion command again.

globalThis.WIFEY_STORY_CONTENT = ${contentLiteral};

globalThis.WIFEY_DEFAULT_FLOW_MAP = globalThis.WIFEY_STORY_CONTENT.defaultFlowMap;
`;
}

function printValidation(label, result) {
  if (result.warnings.length) {
    console.warn(`${label}: validation warnings:`);
    result.warnings.forEach((warning) => console.warn(`- ${warning}`));
  }

  if (!result.errors.length) return;

  console.error(`${label}: validation failed:`);
  result.errors.forEach((error) => console.error(`- ${error}`));
}

const options = parseArgs(process.argv.slice(2));

if (options.help) {
  printUsage();
  process.exit(0);
}

if (!options.sourcePath) {
  printUsage();
  process.exit(1);
}

const raw = await readFile(options.sourcePath, 'utf8');
const source = JSON.parse(raw);
const sourceValidation = validateStoryExport(source);
printValidation(formatPathLabel(options.sourcePath), sourceValidation);

if (sourceValidation.errors.length) {
  process.exit(1);
}

const promotedContent = createPromotedContent(source);
const promotedValidation = validateStoryExport({
  content: promotedContent,
  flowMap: promotedContent.defaultFlowMap,
});
printValidation('promoted package content', promotedValidation);

if (promotedValidation.errors.length) {
  process.exit(1);
}

const output = formatStoryDataModule(promotedContent, options.sourcePath);
const targetLabel = formatPathLabel(options.outputPath);
const { days, envelopes, choices, flowRules } = promotedValidation.stats;

if (options.dryRun) {
  console.log(
    `Promotion dry run passed: ${days} days, ${envelopes} envelopes, ${choices} choices, ${flowRules} flow rules. Target: ${targetLabel}.`,
  );
} else {
  await writeFile(options.outputPath, output, 'utf8');
  console.log(
    `Promoted release content: ${days} days, ${envelopes} envelopes, ${choices} choices, ${flowRules} flow rules written to ${targetLabel}.`,
  );
}
